<h1>
إبقائها تحت السيطرة
</h1>
<p>
قائمة بحد أقصى 10 مهام لهذا اليوم. "واقعيا ، إلى أي مدى يمكن أن تتناسب مع؟ قد يكون خمسة أو ستة أشياء ، أو واحد فقط. حدد أهم شيء عليك القيام به اليوم والمدة التي ستستغرقها كل مهمة. "اعلم أنك ستقلل من تقدير ذلك تقريبًا. يقول إيفانز: "خصص الوقت في اليوم الذي ستقوم فيه بذلك".
</p>
<p>
<h1>كيف تبدأ</h1>
يوصي إيفانز بمعالجة المهمة التي تتطلع إليها أولاً. ينصح روي بوميستر ، أستاذ علم النفس بجامعة كوينزلاند والمؤلف المشارك لـ "قوة الإرادة: إعادة اكتشاف أعظم قوة بشرية" ، بما في ذلك "بعض الأشياء السهلة بالإضافة إلى الأشياء الأصعب. يمكنك أن تبدأ على الفور في طريقتين سهلتين ، وتخرج من الطريق ، وتشعري بإحراز تقدم. لكن بالنظر إلى أن قوة الإرادة محدودة ، يجب ألا تبدد كل شيء على السهلة - تعامل مع هؤلاء على أنهم عمليات احماء. "يجب أن تكون المهام محددة وصغيرة ، كما يقول. "بدلاً من" تنظيف وطلاء الجراج "، ربما ابدأ بـ" شراء الطلاء "و" ملء برميل قمامة واحد "، وهكذا."
<h1>
كم عدد القوائم؟</h1>

<p>
من الناحية المثالية ، كما يقول مورغنستيرن ، لن تحتفظ بقائمة مهام على الإطلاق - "يجب دمج مهامك في التقويم الخاص بك على الفور. أنها تمكنك من حساب وتخطيط أيامك بشكل واقعي. لكل مهمة ، ما المدة التي ستستغرقها ومتى ستقوم بذلك؟ صباح الاربعاء؟ بعد ظهر يوم السبت؟ ثم ليس لديك قائمة ، ولكن خطة ".
</p>
<p>
