<h1>يا له من المهام! كيف تكتب القائمة المثالية</h1>
<img src="40.jpg" >
<p>

إنها بسيطة وأنيقة ومرتبة. من الناحية النظرية ، فإن قائمة المهام هي الحل لجميع مشكلات الإنتاجية لديك. أولاً ، اكتب "قائمة المهام" في الأعلى ، فقط حتى يكون لديك شيء لتضعه في الحال ، ثم اذكر كل الأشياء التي تحتاجها لإنجازها. اعمل في طريقك ، ثم تشمس في مجد إنجازك.
</p>
<p>
باستثناء ، بالنسبة للكثيرين منا ، فإن قوائمنا لا تنتهي أبدا. تقول جولي مورجنستيرن ، خبيرة تنظيمية وإنتاجية ومؤلفة إدارة الوقت من جولي ، "لهذا السبب لا أحب قوائم المهام - لأنها تصبح ساحقة لدرجة أنها تخيف ثم لا ترغب أبدًا في النظر إليها". الداخل الى الخارج. "إنها مكان لالتقاط المهام ، ولكن ليس لتنفيذ المهام."
</p>
<p>
يوافق كلير إيفانز ، مدرب إدارة الوقت والإنتاجية ، على ذلك. إنها لا تحب استدعاء "قوائم المهام". "أنا أشير إليهم كقوائم إجراءات" ، كما تقول. لإنشاء قائمة إجراءات فعالة ، قم بملئها بالأشياء التي ستعمل عليها حقًا ؛ لا تنشئ مخزونًا ضخمًا من المهام العشوائية ، وربما غير المهمة.
</p>
