واجه أكثر من نصف المستهلكين البريطانيين خرق البيانات العام الماضي
</h1>
<img src="img/47.png" >
<p>
كشفت الأبحاث الجديدة أن أكثر من نصف البريطانيين وقعوا ضحية لانتهاك البيانات خلال العام الماضي.
</p>
<p>
وفقًا لتقرير صادر عن DynaRisk ، والذي قام بإجراء مسح ضوئي لأكثر من 750،000 قطعة بيانات ، واجه المستخدمون في المملكة المتحدة المزيد من التهديدات مقارنة بالمستخدمين في نيوزيلندا أو كندا أو أوروبا.
</p>
<p>
وجدت البيانات أن 58 في المائة من البريطانيين أصبحوا ضحية لانتهاك البيانات في الأشهر الـ 12 الماضية ، وأن أولئك الذين لديهم عنوان بريد إلكتروني BT كانوا الأكثر عرضة للانتهاك الذي تبعه Hotmail و Gmail و Yahoo.
</p>
<p>
ويشير البحث ، الذي جمع البيانات من أكثر من 1200 مستخدم ، أيضًا إلى أن الوعي حول الأمن السيبراني وصحة البيانات قد تحسن بشكل كبير في المملكة المتحدة. وينص أيضًا على أن المستخدمين أكثر نشاطًا في حماية معلوماتهم الرقمية عند مقارنتها بالمستخدمين الأمريكيين أو الأوروبيين.
</p>
