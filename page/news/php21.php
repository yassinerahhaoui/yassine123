مفصل موتورولا رازر "كسر" بعد 27000 طيات في اختبار المتانة

</h1>
<img src="img/52.jpg" >
<p>
نظرًا لأن الهواتف القابلة للطي لا تزال غير شائعة ، يرغب الكثير من المشترين المحتملين في معرفة مدى متانتها قبل تسديد الأموال مقابل واحدة - خاصة وأن الطرز المتاحة ليست رخيصة الثمن. CNET وقد وضع جديد موتورولا رازر لل اختبار من قبل للطي مرارا وتكرارا لآلاف المرات باستخدام جهاز يسمى Foldbot التي سكوير. النتيجة؟ بدأت مفصليها تتصرف متزعزع بنحو 27000 أضعاف. لاحظت دراسة أجريت عام 2017 أن الأميركيين يفحصون هواتفهم 80 مرة في اليوم. هذا يعني أن الرازر يمكن أن يبدأ في إظهار المشاكل في غضون عام.
</p>
